public class countingAkm {
    //28
    public static void main(String[] args) {
        System.out.println("Printing natural number till : ");
        Scanner sc =new Scanner(System.in);
        int n = sc.nextInt();
        for (int i = 0; i <= n; i++) {
            System.out.println(i);
        }
    }
}
